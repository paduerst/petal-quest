<ul>
	<li>
		A prone creature’s only movement option is to crawl, unless it stands up and thereby ends the
		condition.
	</li>
	<li>The creature has disadvantage on attack rolls.</li>
	<li>
		An attack roll against the creature has advantage if the attacker is within 5 feet of the
		creature. Otherwise, the attack roll has disadvantage.
	</li>
</ul>

<div class="card mt-3 p-4 pt-2 variant-ghost-tertiary">
	<h2>Being Prone</h2>
	<p>
		Combatants often find themselves lying on the ground, either because they are knocked down or
		because they throw themselves down. In the game, they have the prone condition.
	</p>
	<p>
		You can <b>drop prone</b> without using any of your speed. <b>Standing up</b> takes more effort;
		doing so costs an amount of movement equal to half your speed. For example, if your speed is 30 feet,
		you must spend 15 feet of movement to stand up. You can’t stand up if you don’t have enough movement
		left or if your speed is 0.
	</p>
	<p>
		To move while prone, you must <b>crawl</b> or use magic such as teleportation. Every foot of movement
		while crawling costs 1 extra foot. Crawling 1 foot in difficult terrain, therefore, costs 3 feet
		of movement.
	</p>
</div>
