<p>
	If an already exhausted creature suffers another effect that causes exhaustion, its current level
	of exhaustion increases by the amount specified in the effect’s description.
</p>
<p>
	A creature suffers the effect of its current level of exhaustion as well as all lower levels. For
	example, a creature suffering level 2 exhaustion has its speed halved and has disadvantage on
	ability checks
</p>
<p>
	An effect that removes exhaustion reduces its level as specified in the effect’s description, with
	all exhaustion effects ending if a creature’s exhaustion level is reduced below 1.
</p>
<p>
	Finishing a long rest reduces a creature’s exhaustion level by 1, provided that the creature has
	also ingested some food and drink.
</p>
